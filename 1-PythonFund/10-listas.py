# Inicio do estudo das COLEÇÕES em Python
# Em uma LISTA Podemos armazenar dados HETEROGÊNEOS["Fifa 23", 2025, 10.0, True] Ex: 'String', int, float, bolean

gameList = ["Resident Evil 4", "Star Wars", "Zelda", "Red Dead 2", "Mario Card"]
print(gameList)

# 1 - Buscar os dois primeiros itens da lista
print(gameList[:2])

# 2 - Buscar o último item da lista
print(gameList[-1])

# 3 - Buscar jogos até uma determinada posição
print(gameList[1:4])

# 4 - Buscar jogos de uma posição em diante
print(gameList[1:])